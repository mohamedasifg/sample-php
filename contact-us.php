<script>
      $(document).ready(function () {
    $('#emailForm').submit(function (event) {
        event.preventDefault(); // Prevent the default form submission behavior
        sendEmail();
    });

    function sendEmail() {
        var formData = $('#emailForm').serialize();

        $.ajax({
            type: 'POST',
            url: 'send_mail/send_email.php', // Update the URL to your PHP script
            data: formData,
            dataType: 'json',
            success: function (response) {
                if (response.status === "Success") {
                    // Show a success message with SweetAlert
                    Swal.fire({
                        icon: "success",
                        title: "Success",
                        text: "Your response has been recorded. Thanks for subscribing to our newsletter.",
                        timer: 5000
                    });
                } else if (response.status === "Error") {
                    // Show an error message with SweetAlert
                    Swal.fire({
                        icon: "error",
                        title: "Error",
                        text: "Form submission failed. Please try again.",
                        timer: 5000
                    });
                }
            },
            error: function () {
                // Handle AJAX request errors with SweetAlert
                Swal.fire({
                    icon: "error",
                    title: "Error",
                    text: "An error occurred while processing your request. Please try again later.",
                    timer: 5000
                });
            }
        });
    }
});

