<script>
  $(document).ready(function () {
    $('#newsletterForm').submit(function (event) {
      event.preventDefault(); // Prevent the default form submission behavior
      sendEmail();
    });

    function sendEmail() {
      var formData = $('#newsletterForm').serialize();

      $.ajax({
        type: 'POST',
        url: 'send_mail/send_newsletter.php',
        data: formData,
        dataType: 'json',
        success: function (response) {
          if (response.status === "Success") {
            Swal.fire({
              icon: "success",
              title: "Success",
              text: "Your response has been recorded. Thanks for subscribing to our newsletter.",
              showConfirmButton: true,
              showCloseButton: true, // Show the close button
              timer: 6000 // Auto close the success message after 5 seconds
            });
          } else if (response.status === "Error") {
            Swal.fire({
              icon: "error",
              title: "Error",
              text: "Form submission failed. Please try again.",
              showCloseButton: true // Show the close button
            });
          }
        }
      });
    }
  });
</script>

